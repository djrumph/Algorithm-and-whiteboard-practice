function findLongestWord(str) {
  newStr = str.split(" ");
  var longest = 0;
  
 for(var i =0; i<newStr.length; i++)
    {
      
      if (newStr[i].length >longest)
        {
          longest = newStr[i].length;
        }
    }
  
  return longest;
  
  //return str.length;
  
  
}

findLongestWord("The quick brown fox jumped over the lazy dog");
